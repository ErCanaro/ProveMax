    private void cargarFormularioAlSeleccionarFila(){
         if(jTProductos.getSelectedRow()!=-1 && jTProductos.isEnabled()){
           Producto producto = prodData.buscarProductoPorId((int)jTProductos.getValueAt(jTProductos.getSelectedRow(),0));
           
            jTFIdProducto.setText(String.valueOf(producto.getIdProducto()));
            jTFNombre.setText(producto.getNombre());
            jTFDescripcion.setText(producto.getDescripcion());
            jTFPrecioActual.setText(String.valueOf(producto.getPrecioActual()));
            jTFStock.setText(String.valueOf(producto.getStock()));
            jTFStockMinimo.setText(String.valueOf(producto.getStockMinimo()));
            jRBEstado.setSelected(producto.isEstado());
        }
    }
